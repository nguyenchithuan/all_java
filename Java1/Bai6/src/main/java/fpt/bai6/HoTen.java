package fpt.bai6;

public class HoTen {
    private String ho;
    private String dem;
    private String ten;

    public HoTen(String ho, String dem, String ten) {
        this.ho = ho;
        this.dem = dem;
        this.ten = ten;
    }

    public HoTen() {
    }
    
    

    public String getHo() {
        return ho;
    }

    public void setHo(String ho) {
        this.ho = ho;
    }

    public String getDem() {
        return dem;
    }

    public void setDem(String dem) {
        this.dem = dem;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
    
    
}
